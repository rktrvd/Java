public class J09_Opr_presedence {
    public static void main(String[] args) {
        int a = 6*5-34/2;
        /*
           =30-34/2
           =30-17
           =12
         */
        System.out.println(a);

        int b = 60/5-34*2;
        /*
           =12-34*2
           =12-68
           =-56
         */
        System.out.println(b);

        // Precedence & Associativity

        
    }
}
